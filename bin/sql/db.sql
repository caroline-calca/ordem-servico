CREATE TABLE CLIENTE (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  NOME VARCHAR(120) NOT NULL,
  DOCUMENTO VARCHAR(20),
  EMAIL VARCHAR(120),
  TELEFONE VARCHAR(30),
  DATA_CADASTRO TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

CREATE TABLE ORDEM_SERVICO (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  CLIENTE_ID INTEGER NOT NULL,
  DATA_ABERTURA DATE DEFAULT CURRENT_DATE NOT NULL,
  DATA_PREVISTA DATE,
  DATA_FECHAMENTO DATE,
  STATUS VARCHAR(15) NOT NULL,
  DESCRICAO_PROBLEMA VARCHAR(500),
  VALOR_TOTAL NUMERIC(15,2) DEFAULT 0,
  CONSTRAINT FK_OS_CLIENTE FOREIGN KEY (CLIENTE_ID) REFERENCES CLIENTE(ID),
  CONSTRAINT CHK_OS_STATUS CHECK (STATUS IN ('Aberta','Em Andamento','Concluida','Cancelada'))
);

COMMENT ON COLUMN ORDEM_SERVICO.STATUS IS 'Aberta|Em Andamento|Concluida|Cancelada';

CREATE TABLE ITEM_ORDEM (
  ID INTEGER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  ORDEM_ID INTEGER NOT NULL,
  DESCRICAO VARCHAR(200) NOT NULL,
  QUANTIDADE NUMERIC(12,2) NOT NULL,
  VALOR_UNITARIO NUMERIC(15,2) NOT NULL,
  CONSTRAINT FK_ITEM_ORDEM FOREIGN KEY (ORDEM_ID) REFERENCES ORDEM_SERVICO(ID) ON DELETE CASCADE
);

CREATE INDEX IDX_OS_STATUS ON ORDEM_SERVICO (STATUS);
CREATE INDEX IDX_OS_CLIENTE ON ORDEM_SERVICO (CLIENTE_ID);
CREATE INDEX IDX_OS_DATA_ABERTURA ON ORDEM_SERVICO (DATA_ABERTURA);

CREATE VIEW VW_OS_RESUMO AS
SELECT
  O.ID,
  O.CLIENTE_ID,
  C.NOME AS CLIENTE_NOME,
  O.DATA_ABERTURA,
  O.DATA_PREVISTA,
  O.DATA_FECHAMENTO,
  O.STATUS,
  O.VALOR_TOTAL,
  CASE
    WHEN (O.STATUS NOT IN ('Concluida','Cancelada'))
     AND (O.DATA_PREVISTA IS NOT NULL)
     AND (CURRENT_DATE > O.DATA_PREVISTA)
    THEN 1 ELSE 0 END AS EM_ATRASO
FROM ORDEM_SERVICO O
JOIN CLIENTE C ON C.ID = O.CLIENTE_ID;
